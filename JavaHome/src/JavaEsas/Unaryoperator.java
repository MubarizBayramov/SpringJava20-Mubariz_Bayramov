package JavaEsas;

public class Unaryoperator {

	
	
	    public static void main(String[] args) {
	        int a = 5;
	        System.out.println(+a);  // 5 (dəyişiklik yoxdur)
	        System.out.println(-a);  // -5 (mənfi edir)

	        a++;  // a = a + 1 → 6
	        System.out.println(a); // 6

	        --a;  // a = a - 1 → 5
	        System.out.println(a); // 5

	        boolean b = true;
	        System.out.println(!b); // false (NOT operatoru)
	    }
	}


