package JavaEsas;

public class whiledovru {
	
	    public static void main(String[] args) {
	        int i = 1; // Başlanğıc dəyəri
	        
	        while (i <= 5) { // Şərt: i 5-dən kiçik və ya bərabər olmalıdır
	            System.out.println("Ədəd: " + i);
	            i++; // i-ni bir vahid artırırıq
	        }
	    }
	    
}  
	    
	  class DoWhileExample {
	        public static void main(String[] args) {
	            int i = 1; // Başlanğıc dəyəri
	            
	            do {
	                System.out.println("Ədəd: " + i);
	                i++; // i-ni bir vahid artırırıq
	            } while (i <= 5); // Şərt sonda yoxlanılır
	        }
	    }

	



